import streamlit as st
import requests
import datetime

BASE_URL = "http://localhost:8000"  # FastAPI backend

# ğŸ¨ Page configuration
st.set_page_config(
    page_title="Travel Planner AI",
    page_icon="âœˆï¸",
    layout="centered",
    initial_sidebar_state="expanded",
)

# âœ¨ Header
st.markdown("<h1 style='text-align: center; color: #4CAF50;'>ğŸŒ Travel Planner AI Agent</h1>", unsafe_allow_html=True)
st.markdown("<p style='text-align: center;'>Plan your dream trip in minutes using AI âœ¨</p>", unsafe_allow_html=True)
st.divider()

# ğŸ“ Chat input + messages
st.markdown("### ğŸ“ How can I help to plan your next trip?")
st.markdown("*Try something like:* `Plan a 7-day honeymoon to Bali on a low budget.`")

# Store chat messages
if "messages" not in st.session_state:
    st.session_state.messages = []

# Chat input form
with st.form(key="query_form", clear_on_submit=True):
    user_input = st.text_input("Enter your query:", placeholder="e.g. Plan a trip to Goa for 5 days")
    submit_button = st.form_submit_button("ğŸ¤ Ask AI")

# Handle response
if submit_button and user_input.strip():
    with st.spinner("ğŸ¤– Thinking... Generating your travel plan..."):
        try:
            payload = {"question": user_input}
            response = requests.post(f"{BASE_URL}/query", json=payload)

            if response.status_code == 200:
                answer = response.json().get("answer", "No answer returned.")

                # ğŸ§¾ Display nicely formatted response
                st.success("âœ… Here's your travel plan!")
                with st.expander("ğŸ“„ View Full Plan", expanded=True):
                    st.markdown(f"""
ğŸ§­ Question:  {user_input}  
ğŸ“Œ Answer:   

{answer}  

---

ğŸ—“ï¸ **Generated:** {datetime.datetime.now().strftime('%Y-%m-%d at %H:%M')}  
ğŸ‘¤ **Created by:** Udara's Travel Agent  
ğŸ“§ **Contact:** udara@travelagent.com  

> *This travel plan was generated by AI. Please verify all prices, schedules, and entry requirements before booking your trip.*
                    """)

            else:
                st.error("ğŸš¨ Bot failed to respond: " + response.text)

        except Exception as e:
            st.error(f"ğŸš« Something went wrong: {e}")
            
# Initialize chat history
if "messages" not in st.session_state:
    st.session_state.messages = []
    

# Display chat history
if st.session_state.messages:
    st.markdown("### ğŸ—¨ï¸ Chat History")
    for msg in st.session_state.messages:
        st.markdown(f"**User:** {msg['user']}  \n**Bot:** {msg['bot']}")
        st.session_state.messages.append({"user": user_input, "bot": answer})
        
# Add a footer
